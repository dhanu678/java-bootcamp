public class Datatype {
    public static void main (String[] args){

        Byte a = 3;
        long b = 120L;

        System.out.println(a + b );
        System.out.println(a - b );
        System.out.println(a * b );
        System.out.println(a / b );
        System.out.println(a % b );

    }
}
 
