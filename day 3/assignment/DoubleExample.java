class DoubleExample{

           public static void main(String[]args){
           
           double a = 10.1;
           double b = 3.1;

      System.out.println(a+b);
      System.out.println(a-b);
      System.out.println(a*b);
      System.out.println(a/b);
      System.out.println(a%b);
   }

}	
   
